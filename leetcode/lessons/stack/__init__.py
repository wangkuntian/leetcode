def neighbors(cur):
    return []


def DFS(root, target):
    visited = set()
    # add root to stack
    stack = [root]
    while stack:
        cur = stack.pop()
        if cur == target:
            return True
        for node in neighbors(cur):
            if node not in visited:
                stack.append(node)
                visited.add(node)
    return False



