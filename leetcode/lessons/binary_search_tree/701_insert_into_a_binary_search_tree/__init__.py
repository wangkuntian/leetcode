"""
难度：中等

给定二叉搜索树（BST）的根节点和要插入树中的值，将值插入二叉搜索树。
返回插入后二叉搜索树的根节点。 保证原始二叉搜索树中不存在新值。

注意，可能存在多种有效的插入方式，只要树在插入后仍保持为二叉搜索树即可。
你可以返回任意有效的结果。

例如, 

给定二叉搜索树:

        4
       / \
      2   7
     / \
    1   3

和 插入的值: 5
你可以返回这个二叉搜索树:

         4
       /   \
      2     7
     / \   /
    1   3 5
或者这个树也是有效的:

         5
       /   \
      2     7
     / \
    1   3
         \
          4
"""

from leetcode.lessons.binary_search_tree import TreeNode


class Solution(object):
    def insertIntoBST(self, root, val):
        """
        :type root: TreeNode
        :type val: int
        :rtype: TreeNode
        """
        new = TreeNode(val)

        if not root:
            return new

        node = root

        while True:
            if val < node.val:
                if not node.left:
                    node.left = new
                    return root
                node = node.left
            else:
                if not node.right:
                    node.right = new
                    return root
                node = node.right


root = TreeNode.generate([4, 2, 1, None, None, 3, None, None, 7])
print(Solution().insertIntoBST(root, 5))
