"""
难度：简单

给定一个只包含小写字母的有序数组letters 和一个目标字母 target，
寻找有序数组里面比目标字母大的最小字母。

数组里字母的顺序是循环的。举个例子，如果目标字母target = 'z'
并且有序数组为 letters = ['a', 'b']，则答案返回 'a'。

输入:
letters = ["c", "f", "j"]
target = "a"
输出: "c"

输入:
letters = ["c", "f", "j"]
target = "c"
输出: "f"

输入:
letters = ["c", "f", "j"]
target = "d"
输出: "f"

输入:
letters = ["c", "f", "j"]
target = "g"
输出: "j"

输入:
letters = ["c", "f", "j"]
target = "j"
输出: "c"

输入:
letters = ["c", "f", "j"]
target = "k"
输出: "c"
"""


class Solution(object):
    def nextGreatestLetter(self, letters, target):
        """
        :type letters: List[str]
        :type target: str
        :rtype: str
        """

        left = 0
        right = len(letters) - 1

        while left <= right:
            mid = (left + right) // 2
            if target < letters[mid]:
                right = mid - 1
            else:
                left = mid + 1

        return letters[left % len(letters)]


print(Solution().nextGreatestLetter(["c", "f", "j"], "a"))
