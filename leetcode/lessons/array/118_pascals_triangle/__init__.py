"""
难度：简单

给定一个非负整数numRows，生成杨辉三角的前numRows行。

在杨辉三角中，每个数是它左上方和右上方的数的和。

示例:
输入: 5
输出:
[
     [1],
    [1,1],
   [1,2,1],
  [1,3,3,1],
 [1,4,6,4,1]
]
"""


class Solution(object):
    def generate(self, numRows):
        """
        :type numRows: int
        :rtype: List[List[int]]
        """
        result = [[1]]

        if numRows == 0:
            return []
        for row in range(1, numRows):
            result.append([1])
            for num_1, num_2 in zip(result[-2][:-1], result[-2][1:]):
                result[-1].append(num_1 + num_2)

            result[-1].append(1)

        return result


print(Solution().generate(5))
