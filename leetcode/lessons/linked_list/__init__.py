# Definition for singly-linked list.
class ListNode(object):
    def __init__(self, x):
        self.val = x
        self.next = None

    def __repr__(self):
        temp = self
        s = ''
        while temp:
            s += str(temp.val) + ' ---> '
            temp = temp.next
        s += 'None'
        return s
