"""
难度：简单

给定一个二叉树和一个目标和，判断该树中是否存在根节点到叶子节点的路径，
这条路径上所有节点值相加等于目标和。

说明:叶子节点是指没有子节点的节点。

示例:
给定如下二叉树，以及目标和 sum = 22，
              5
             / \
            4   8
           /   / \
          11  13  4
         /  \      \
        7    2      1
返回 true, 因为存在目标和为 22 的根节点到叶子节点的路径 5->4->11->2。
"""

from leetcode.lessons.binary_tree import TreeNode


class Solution(object):
    def hasPathSum(self, root, sum):
        """
        :type root: TreeNode
        :type sum: int
        :rtype: bool
        """
        if not root:
            return False

        sum -= root.val

        if sum == 0 and not root.left and not root.right:
            return True

        return self.hasPathSum(root.left, sum) \
               or self.hasPathSum(root.right, sum)


root = TreeNode(5)
left = TreeNode(4)
right = TreeNode(8)
root.right = right
root.left = left

left_1 = TreeNode(11)
left.left = left_1
left_1.left = TreeNode(7)
left_1.right = TreeNode(2)

right.left = TreeNode(13)
right_1 = TreeNode(4)
right.right = right_1
right_1.right = TreeNode(1)

print(Solution().hasPathSum(root, 22))
